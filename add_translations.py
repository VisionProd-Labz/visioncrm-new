# Lire le fichier, ajouter les traductions et sauvegarder
def add_translations_to_file(lang_code, translations_dict):
    file_path = f"C:\\Users\\Kuetey\\Documents\\visioncrm\\locales\\{lang_code}.json"

    # Lire le fichier existant
    with open(file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    # Ajouter les nouvelles traductions
    data.update(translations_dict)

    # Sauvegarder avec indentation
    with open(file_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"Traductions ajoutées pour {lang_code}")

# Ajouter les traductions pour le néerlandais
